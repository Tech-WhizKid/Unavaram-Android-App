public class myadapter extends RecyclerView.Adapter<myadapter.myviewholder>
{
    ArrayList<model> datalist;
    FirebaseAuth fAuth= FirebaseAuth.getInstance();
    public String userID = fAuth.getCurrentUser().getUid();
    public String uid;
    FirebaseFirestore db;

    CollectionReference collectionRef = FirebaseFirestore.getInstance().collection("user data");

    DocumentReference dcref = collectionRef.document();

    String userId = FirebaseAuth.getInstance().getCurrentUser().getUid();
    Query query = collectionRef.whereEqualTo("userId", userId);
    public myadapter(ArrayList<model> datalist) {
        this.datalist = datalist;
        db = FirebaseFirestore.getInstance();
    }

    @NonNull
    @Override
    public myviewholder onCreateViewHolder (@NonNull ViewGroup parent,int viewType){
        View view = LayoutInflater.from(parent.getContext()).inflate(R.layout.singlerow, parent, false);
        return new myviewholder(view);
    }

    @Override
    public void onBindViewHolder(@NonNull myviewholder holder, @SuppressLint("RecyclerView") int position) {
        holder.tname.setText(datalist.get(position).getName());
        holder.ttype.setText(datalist.get(position).getType());
        holder.tdescription.setText(datalist.get(position).getDescription());
        holder.delet.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                // Get the ID of the document you want to delete
                String id = datalist.get(position).getId();

                db.collection("user data")
                        .document(id)
                        .delete()
                        .addOnSuccessListener(new OnSuccessListener<Void>() {
                            @Override
                            public void onSuccess(Void unused) {
                                // Remove the deleted item from the datalist
                                datalist.remove(position);
                                // Notify the adapter that the data has changed
                                notifyDataSetChanged();
                            }
                        }).addOnFailureListener(new OnFailureListener() {
                            @Override
                            public void onFailure(@NonNull Exception e) {
                                Log.w(TAG, "Error!", e);
                            }
                        });
            }
        });
    }


    @SuppressLint("NotifyDataSetChanged")
    public void deleteItem(int position){

    }

    @Override
    public int getItemCount() {
        return datalist.size();
    }

    class myviewholder extends RecyclerView.ViewHolder
    {
        TextView tname,ttype,tdescription;
        Button delet;
        public myviewholder(@NonNull View itemView) {
            super(itemView);
            tname = itemView.findViewById(R.id.name);
            ttype = itemView.findViewById(R.id.type);
            tdescription = itemView.findViewById(R.id.description);
            delet = itemView.findViewById(R.id.delete);
        }
    }
}
